					<ul>
					1.Losing excess weight<br>
					If you are overweight, losing 5-10% of your body weight can have a significant impact on PCOS symptoms.
					It also reduces the risk of developing type 2 diabetes and cardiovascular disease.<br>
                    <br>
					2.Physical activity<br>
                    Being physically active is very important in managing PCOS.
					It has been shown to improve symptoms and reduce the risk of developing related long-term health conditions.Research has shown any type of regular exercise is effective in improving PCOS symptoms. 
					Whether it is moderate or vigorous aerobic exercise or resistance (using weights) exercise, women’s PCOS symptoms will improve.<br>
                    <br>
					3.Practice good sleep hygiene<br>
					Sleep affects your stress levels and helps regulate cortisol to balance your hormones.
					 But sleep disturbances are twice as commonTrusted Source for women with PCOS.<br>
                    <br>
					4.Limit or avoid endocrine disruptors<br>
					Endocrine disruptors are chemicals or ingredients that interfere with or block your body’s natural hormonal reactions.
                    Some endocrine disruptors mimic female and male sex hormones, causing confusion in your reproductive system. This can increase your risk of PCOS symptoms.
