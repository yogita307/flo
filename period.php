					<ul>
					In many cases, an endometriosis diagnosis will start with your symptoms.
					Painful and heavy periods might cause you to reach out to your healthcare provider.
					Once at an appointment, your provider (typically an Ob-Gyn) may start by asking you for your personal medical history, about any previous pregnancies and if any other people in your family have endometriosis.
					Your provider may do a pelvic exam. If your healthcare provider needs more information they’ll likely perform pelvic imaging starting with an ultrasound. Depending on your symptoms, physical exam and ultrasound results,
					an MRI may also be ordered for further endometriosis mapping. A laparoscopy may be offered for both definitive diagnosis and treatment. It can be a useful way to confirm endometriosis because your surgeon doing the procedure can use a small camera (laparoscope) to look inside your body.
					A biopsy (small tissue sample) might be taken during this procedure. The biopsy will be sent to a lab to confirm the diagnosis.<br>
					<br>
					Sometimes, you find endometriosis by accident. Not all people who have endometriosis will experience symptoms. 
					In these cases, your provider might discover the condition during a different procedure.
