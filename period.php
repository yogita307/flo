					They can also help your body use insulin more efficiently, lower blood glucose levels, and may help you ovulate.<br>
                    <br>
					2.Medications to cause ovulation<br>
                    Medications can help the ovaries to release eggs normally. These medications also have certain risks. They can increase the chance for a multiple birth (twins or more). And they can cause ovarian hyperstimulation. 
					This is when the ovaries release too many hormones. 
					It can cause symptoms such as abdominal bloating and pelvic pain.<br>
                    <br>
					3.Birth control pills<br>
                    These help to control menstrual cycles, lower androgen levels, and reduce acne.<br>
                    <br>
					4.Diabetes medication<br>
					This is often used to lower insulin resistance in PCOS.
					It may also help reduce androgen levels, slow hair growth, and help you ovulate more regularly.<br>
