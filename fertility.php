					Pregnancy is a complex process. While many women have normal pregnancies, complications can occur.
					However, having a complication does not mean you will not have a healthy baby. It means your provider will monitor you closely and take special care of you and your baby during the remainder of your term.<br>
					Common complications include:<br>
<br>
Diabetes during pregnancy (gestational diabetes).<br>
High blood pressure during pregnancy (preeclampsia). Your provider will talk with you about how to care for yourself if you have preeclampsia.<br>
Premature or preterm changes in the cervix.<br>
Problems with the placenta. It may cover the cervix, pull away from the womb, or not work as well as it should.<br>
Vaginal bleeding.<br>
Early labor.<br>
Your baby is not growing well.<br>
Your baby has medical problems.<br>
