					<ul>
                    Most simple ovulation predictor tools just ask for the date of the first day of your last period and the average length of your menstrual cycles.
					If you don’t know your regular cycle length, most calendars will suggest that you use 28 days. This is considered the average but research shows there is actually significant variation from woman to woman (and even cycle to cycle for some women). 
					A "normal" cycle can range from 21 to 35 days.<br>
					<br>
					More precise ovulation calendars will factor in how long your personal luteal phase is in addition to your average cycle length.
					To correct for any possible miscalculation, add a few days on at the end of your projected fertility window.<br>
					<br>
					While online ovulation calculators are available, you can also do the same math yourself to determine your most fertile days rather than relying on an online prediction tool.<br>
					<br>
					In fact, women (and doctors) have been doing these calculations on paper for decades. 
					Just follow the example above but plug in your personal last period start date, cycle length, and luteal phase length (if unknown, use 14 days). 
                    For more help on this, see ovulation prediction charting, and to confirm you are doing it correctly for your particular medical situation, consult with your doctor.<br>
					<br>
					If you’re utilizing an ovulation chart to time when to use an ovulation predictor kit (OPK), start using testing a couple of days before you expect to ovulate to be sure that you catch the LH surge the precedes ovulation.
					</ul>
