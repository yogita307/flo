					<ul>
					Make your own ovulation charting by following these simple steps:<br>
					<br>
					1.Consider:
 the information in the chart as a best-guess estimate. If you want a more accurate prediction of ovulation day, try basal body temperature charting.
Know the average number of days in your cycle. This is the number of days from when one period starts and the next begins. The best way to estimate this is by charting your periods over several months.<br>
<br>
Remember: that you are most fertile one to two days before you ovulate. Your odds of pregnancy during your fertile period increase the closer you are to these optimal few days but pregnancy can still occur in the wider fertility window.<br>
<br>
Understand: how to count cycle days. The day you get your period is Cycle Day 1; 15 days later is Cycle Day 15, and so on. The last day of your cycle is the day before you get your next period and is indicative of the length of your cycle.<br>
<br>
Track: your fertility window by plotting your cycle on a calendar, then counting back 14 days (or use your luteal phase length, if known) from the expected last day of your next cycle.<br>
<br>
Add:three or four days (or more, if you want extra cushion) on either side—especially prior to estimated ovulation day and that's your likely fertility window. The key days are the two or three days prior to ovulation but it's often best to hedge your bets by adding a few extra days.
